#Here we are going to learn how to return the data you want to remove. To save it for later
days_of_the_week = ["Monday","Tuesday","Wednesday","Thursday","Friday","Saturday","Sunday"]

removed_element = days_of_the_week.pop(2)
print("We just removed", removed_element, "from the list")



###########################
#What we just did was:
#we removed something ("the element Friday") from the lists "days_of_the_week" and stored it in a variable (we can store it anywhere we want)
#We can only pass in numbers as the argument of the method pop.