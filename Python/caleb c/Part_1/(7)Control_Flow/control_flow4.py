for i in range (21,43):  #prints from 21 to 42
    print(i, end=" ")
print()

for i in range (20,44,2):  #starts printing in the step of 2 [(adds "+2" to 20 continously) up to 44(remember 44 is not included)]
    print(i, end=" ")
print()

for i in range (20,0,-1): #prints backwards. [(adds "-1" to 20) and stops at 0 (remember "0" is not included)]
    print(i, end=" ")
print()

